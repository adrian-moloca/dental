/**
 * Calculate total MRR (Monthly Recurring Revenue)
 */
export const calculateMRR = (): number => {
  return mockSubscriptions
    .filter((sub) => sub.status === 'active')
    .reduce((total, sub) => {
      if (sub.billingCycle === 'yearly') {
        return total + sub.monthlyPrice; // Already stored as monthly equivalent
      }
      return total + sub.monthlyPrice;
    }, 0);
};

/**
 * Get dashboard statistics
 */
export const getDashboardStats = () => {
  const activeOrgs = mockOrganizations.filter((org) => org.status === 'active').length;
  const activeCabinets = mockCabinets.filter((cab) => cab.status === 'active').length;
  const activeSubscriptions = mockSubscriptions.filter((sub) => sub.status === 'active').length;
  const trialSubscriptions = mockSubscriptions.filter((sub) => sub.status === 'trial').length;

  return {
    totalOrganizations: mockOrganizations.length,
    activeOrganizations: activeOrgs,
    totalCabinets: mockCabinets.length,
    activeCabinets,
    totalSubscriptions: mockSubscriptions.length,
    activeSubscriptions,
    trialSubscriptions,
    mrr: calculateMRR(),
    totalUsers: mockUsers.length,
    activeUsers: mockUsers.filter((user) => user.status === 'active').length,
  };
};
