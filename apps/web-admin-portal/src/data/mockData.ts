export const getSubscriptionsByOrganization = (organizationId: string): Subscription[] => {
  return mockSubscriptions.filter((sub) => sub.organizationId === organizationId);
};

/**
 * Get plan by ID
 */
export const getPlanById = (planId: string): Plan | undefined => {
  return mockPlans.find((plan) => plan.id === planId);
};

/**
 * Get module by ID
 */
export const getModuleById = (moduleId: string): Module | undefined => {
  return mockModules.find((module) => module.id === moduleId);
};

/**
 * Get modules by IDs
 */
export const getModulesByIds = (ids: string[]): Module[] => {
  return mockModules.filter((m) => ids.includes(m.id));
};

/**
 * Get modules for a plan
 */
export const getModulesForPlan = (planId: string): Module[] => {
  const plan = getPlanById(planId);
  if (!plan) return [];
  return mockModules.filter((module) => plan.includedModules.includes(module.id));
};

